    self.keys = @[@"姓名",@"电话",@"微信",@"QQ"];
    Staff* staff = self.staff;
    self.values = [NSArray arrayWithObjects:staff.name,[[NSNumber alloc] initWithLongLong: staff.phone],staff.wechat,[[NSNumber alloc] initWithLongLong:staff.qq], nil];
    
}

